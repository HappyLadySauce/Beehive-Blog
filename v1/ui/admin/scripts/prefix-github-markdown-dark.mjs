/**
 * Prefixes github-markdown-dark.css selectors with `html.dark` so dark markdown
 * styles apply when the admin uses class-based theme (next-themes), not OS media.
 *
 * Run from ui/admin: pnpm run generate:github-md
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import postcss from 'postcss';
import prefixSelector from 'postcss-prefix-selector';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const adminRoot = path.join(__dirname, '..');
const input = path.join(adminRoot, 'node_modules/github-markdown-css/github-markdown-dark.css');
const output = path.join(adminRoot, 'src/pages/article/github-markdown-dark-scoped.css');

const source = fs.readFileSync(input, 'utf8');
const result = await postcss([
  prefixSelector({
    prefix: 'html.dark',
    transform(prefix, selector, prefixedSelector) {
      if (selector.startsWith('@')) {
        return selector;
      }
      return prefixedSelector;
    },
  }),
]).process(source, { from: input });

const banner = `/* Generated by scripts/prefix-github-markdown-dark.mjs — do not edit by hand */\n`;
fs.writeFileSync(output, banner + result.css, 'utf8');
console.log('Wrote', path.relative(adminRoot, output));
